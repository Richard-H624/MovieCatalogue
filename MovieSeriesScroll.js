let allSeries = [];
let currentSeries = null;
let currentEpisode = 0;

fetch('/api/movieseries')
  .then(res => res.json())
  .then(seriesList => {
    const grid = document.getElementById('series-grid');
    grid.innerHTML = '';
    seriesList.forEach(series => {
      const col = document.createElement('div');
      col.className = 'col-md-4 mb-4';
      col.innerHTML = `
        <div class="card h-100 shadow-sm">
          <img src="${series.episodes[0]?.poster || ''}" class="card-img-top" alt="${series.name}">
          <div class="card-body">
            <h5 class="card-title">${series.name}</h5>
            <p class="card-text">${series.episodes[0]?.summary || ''}</p>
          </div>
        </div>
      `;
      grid.appendChild(col);
    });
  });
  
function openDetail(seriesIdx, episodeIdx) {
  currentSeries = allSeries[seriesIdx];
  currentEpisode = episodeIdx;
  showEpisode();
  document.getElementById('series-detail').style.display = '';
  document.querySelector('.grid-container').style.display = 'none';
}

function showEpisode() {
  const ep = currentSeries.episodes[currentEpisode];
  document.getElementById('movie-poster').src = ep.poster;
  document.getElementById('movie-title').textContent = ep.title;
  document.getElementById('movie-summary').innerHTML = ep.summary;
  document.getElementById('movie-budget').innerHTML = ep.budget || '';
}

document.getElementById('back-to-grid').onclick = () => {
  document.getElementById('series-detail').style.display = 'none';
  document.querySelector('.grid-container').style.display = '';
};

document.getElementById('prev-movie').onclick = () => {
  if (currentEpisode > 0) {
    currentEpisode--;
    showEpisode();
  }
};
document.getElementById('next-movie').onclick = () => {
  if (currentEpisode < currentSeries.episodes.length - 1) {
    currentEpisode++;
    showEpisode();
  }
};
document.getElementById('prev-series').onclick = () => {
  let idx = allSeries.indexOf(currentSeries);
  if (idx > 0) {
    openDetail(idx - 1, 0);
  }
};
document.getElementById('next-series').onclick = () => {
  let idx = allSeries.indexOf(currentSeries);
  if (idx < allSeries.length - 1) {
    openDetail(idx + 1, 0);
  }
};